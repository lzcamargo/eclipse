package integracao;
public class GitTudo {
	private String nomeGitudo;
	private String dataGitTudo;
	
	public GitTudo(String nomeGitudo, String dataGitTudo) {
		this.nomeGitudo = nomeGitudo;
		this.dataGitTudo = dataGitTudo;
	}

	public String getNomeGitudo() {
		return nomeGitudo;
	}

	public String getDataGitTudo() {
		return dataGitTudo;
	}

	
}
